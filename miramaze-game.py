    clock = pygame.time.Clock()
    play_music(title_music)

    while running:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                running = False
            elif event.type == pygame.MOUSEBUTTONDOWN:
                if title_screen and music_button_2.is_clicked(event.pos):
                    play_boring1()
                    title_screen = False
                elif title_screen and music_button_1.is_clicked(event.pos):
                    play_guitar()
                    title_screen = False
                else:
                    continue

        if title_screen:
            screen.fill(BLACK)
            draw_title_screen(screen)
            music_button_1.draw(screen)
            music_button_2.draw(screen)
            pygame.display.flip()
        else:
            keys = pygame.key.get_pressed()
            if keys[pygame.K_UP]:
                player_pos = move_player(player_pos, "UP")
            if keys[pygame.K_DOWN]:
                player_pos = move_player(player_pos, "DOWN")
            if keys[pygame.K_LEFT]:
                player_pos = move_player(player_pos, "LEFT")
            if keys[pygame.K_RIGHT]:
                player_pos = move_player(player_pos, "RIGHT")

            # Move monsters
            for monster in monsters:
                monster.monster_go()

            # Update the player rectangle position
            player_rect.topleft = (player_pos[1] * player_size, player_pos[0] * player_size)

            # Draw everything
            screen.fill(WHITE)
            draw_maze(screen, current_maze)
            following_object.follow_player(player_pos)
            following_object.draw(screen)
            for monster in monsters:
                monster.draw(screen)
            pygame.display.flip()

            # Check if the monster is touching the player
            for monster in monsters:
                if following_object.is_touching(monster):
                    current_level = len(all_mazes)
                    game_over = True

            # Check if player has reached the goal
            if player_pos == goal_pos:
                game_over = True

            # Game over screen
            if game_over:
                screen.fill(BLACK)
                font = pygame.font.SysFont(None, 48)
                current_level += 1
                if current_level < len(all_mazes):
                    game_over_text = font.render("Next Level", True, WHITE)
                    screen.blit(game_over_text, (SCREEN_WIDTH // 2 - game_over_text.get_width() // 2, SCREEN_HEIGHT // 2 - game_over_text.get_height() // 2))
                    pygame.display.flip()
                    time.sleep(1)
                    current_maze = all_mazes[current_level]
                    player_pos = [1, 1]  # Starting position (row, col)
                    game_over = False
                else:
                    game_over_text = font.render("Game Over", True, WHITE)
                    screen.blit(game_over_text, (SCREEN_WIDTH // 2 - game_over_text.get_width() // 2, SCREEN_HEIGHT // 2 - game_over_text.get_height() // 2))
                    pygame.display.flip()
                    time.sleep(0.8)
                    running = False
        clock.tick(30)

