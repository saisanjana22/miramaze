class FollowingObject:
    def __init__(self, pos, size, color, speed):
        self.pos = pos
        self.size = size
        self.color = color
        self.speed = speed

    def draw(self, screen):
        pygame.draw.rect(screen, self.color, pygame.Rect(self.pos[1] * self.size, self.pos[0] * self.size, self.size, self.size))

    def follow_player(self, player_pos):
        # Follow the player's position
        time.sleep(0.1)
        self.pos = player_pos

    def monster_go(self):
        direction1 = random.choice(["UP", "DOWN", "LEFT", "RIGHT"])
        new_pos = move_monster(self.pos, direction1)
        self.pos = new_pos
    
    def is_touching(self, other):
        return self.pos == other.pos

def move_monster(monster_pos, direction):
    new_pos = monster_pos[:]
    if direction == "UP":
        new_pos[0] -= 1
    elif direction == "DOWN":
        new_pos[0] += 1
    elif direction == "LEFT":
        new_pos[1] -= 1
    elif direction == "RIGHT":
        new_pos[1] += 1

    # Ensure new position is within maze boundaries
    if not (0 <= new_pos[0] < len(current_maze) and 0 <= new_pos[1] < len(current_maze[0])):
        return monster_pos

    # Ensure new position is not a wall
    if current_maze[new_pos[0]][new_pos[1]] == 0:
        return new_pos
    return monster_pos

